// Variables

// Note:
// 1. var is an old way of declaring a variable (that can be changed) and the new method is to use let
// 2. let is required only in the first line, there after this can be ignored. 
// 3. Camel case is the the common convention
// 4. const is used for variables that does not change


let firstName1 = 'Silvester';
let lastName1 = 'Kennedy';

console.log(firstName1);
console.log(lastName1);

// or

let firstName2 = 'Silvester', lastName2 = 'Kennedy';
console.log(firstName2);
console.log(lastName2);
// or

let firstName3, firstName4;
firstName3 = 'Silvester'
lastName3 = 'Kennedy'

console.log(firstName3);
console.log(lastName3);

var fullName = 'Silvester Kennedy';

console.log(fullName);


